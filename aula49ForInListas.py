# A lista também é iteravel
lista = ['Maria', 'Helena', 'Luiz']

for nome in lista:
    print(nome)
# Da onde veio o valor de nome? por ser iterável ela pega o valor de cada e insere em nome
# Ou seja no print vai sair: Maria  
#                            Helena
#                            Luiz


