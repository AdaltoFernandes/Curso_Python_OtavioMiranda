'''
Operadores de atribuição
= += -= *= /= //= **= %=
'''
contador = 2
###
# contador = contador + 1 - abaixo a mesma coisa só que resumida
contador += 1 # Soma direta
print(contador)
contador **= 2  # Elevado
print(contador)
contador //= 5 # divisão inteira
print(contador)
contador /= 5 # divisão normal
print(contador)
